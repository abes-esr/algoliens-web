    /**
     * @Route("/chantier/{ilnCode}-{ilnSecret}/lang", name="view_record_langs")
     * @Entity("iln", expr="repository.findOneBy({'code': ilnCode})")
     */
    public function viewRecordLangs(RecordRepository $recordRepository, Iln $iln)
    {
        $langs = $recordRepository->getLangsForIln($iln);
        $languages_labels = $this->getCodesLangAbes();

        foreach ($langs as $id => $lang) {
            if (isset($languages_labels[$lang["code"]])) {
                $langs[$id]["langlabel"] = $languages_labels[$lang["code"]];
            } else {
                $langs[$id]["langlabel"] = "-";
            }
        }
        return $this->render("lang/index.html.twig", ["langs" => $langs, "iln" => $iln]);
    }

    /**
     * @Route("/chantier/{ilnCode}-{ilnSecret}/lang/{langCode}", name="view_record_lang")
     * @Entity("iln", expr="repository.findOneBy({'code': ilnCode})")
     */
    public function viewRecordOneLang(RecordRepository $recordRepository, Iln $iln, $langCode, Request $request)
    {
        $session = $request->getSession();
        if ($session->get("winnie")) {
            $record = $this->getDoctrine()->getRepository(Record::class)->findOneRandomLang($iln, $langCode);
        } else {
            $record = $this->getDoctrine()->getRepository(Record::class)->findOneRandomLangNoWinnie($iln, $langCode);
        }

        $langs = $this->getCodesLangAbes();
        return $this->redirect(
            $this->generateUrl("view_rcr",
                [
                    "ilnCode" => $record->getRcrCreate()->getIln()->getCode(),
                    "secret" => $record->getRcrCreate()->getIln()->getSecret(),
                    "rcrCode" => $record->getRcrCreate()->getCode(),
                    "ppn" => $record->getPpn()
                ]
            )
        );

        return $this->render("lang/view_record.html.twig", ["langCode" => $langCode, "langLabel" => $langs[$langCode], "iln" => $iln, "record" => $record]);
    }
